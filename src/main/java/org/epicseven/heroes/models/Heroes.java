package org.epicseven.heroes.models;

public class Heroes {

    private String name;

    public Heroes(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return name;
    }
}
